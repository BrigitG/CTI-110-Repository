import turtle
t=turtle.Turtle()
t.speed(2)

t.penup()
t.goto(-30,50) 
t.pendown()
t.pensize(10)
t.pencolor("pink")
t.right(90)
t.forward(200)
t.penup()
t.goto(-30,50)
t.pendown()
t.right(-90)
t.circle(-50,180)
t.penup()
t.goto(-30,-50)
t.pendown()
t.right(180)
t.circle(-50,180)
t.clear()
t.pen()
t.clear()

t=turtle.Turtle()
t.pencolor("purple")
t.speed(2)
t.pensize(10)
t.penup()
t.goto(150,0)
t.pendown()
for x in range(180):
    t.backward(1)
    t.left(1)
t.right(90)
t.forward(50)
t.right(90)
t.forward(30)
t.right(90)
t.forward(50)


turtle.done()

